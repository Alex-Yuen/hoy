package ws.hoyland.rstest;

public class Record {
	private long id;
	private long age;
	private long nation;
	private long zone;
	private long family;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public long getAge() {
		return age;
	}
	public void setAge(long age) {
		this.age = age;
	}
	public long getNation() {
		return nation;
	}
	public void setNation(long nation) {
		this.nation = nation;
	}
	public long getZone() {
		return zone;
	}
	public void setZone(long zone) {
		this.zone = zone;
	}
	public long getFamily() {
		return family;
	}
	public void setFamily(long family) {
		this.family = family;
	}
	
	
}
